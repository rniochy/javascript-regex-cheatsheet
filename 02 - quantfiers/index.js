/**
 * We use Quantifers to quantify how many  matches we have in our pattern   
 * 
 *  * - Zero ou more matches
 *  + - One ou more matches
 *  ? - Zero ou one
 *  {} - is to match the amount defined 
 *  {2} - exactly 2 
 *  {2, 5} - Between  2 an 5
 *  {2, } -2 or more 
 * */
